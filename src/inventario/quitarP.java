    public void complet()
    {
        String var;
        TextAutoCompleter comple=new TextAutoCompleter(txt_Id);
        try {
            st = cn.createStatement();
            rs = st.executeQuery("SELECT NumPart FROM partes");
            while (rs.next()) {
                var=rs.getString("NumPart");
                comple.addItem(var);
            }
        } catch (Exception e) {
            System.out.println("Error En : " + e.getMessage());
        }
    }
    
    public void buscar()
    {
        String var=txt_Id.getText().toUpperCase().trim();
        Consultar = "SELECT Cantidad FROM partes WHERE NumPart = '" + var + "'";
        try {
            st = cn.createStatement();
            rs = st.executeQuery(Consultar);
            if (rs.next()) {
                cant=rs.getString("Cantidad");
            }
        } catch (Exception e) {
            System.out.println("Error En Buscar :" + e.getMessage());
        }
    }
