        buscar();
        Modificar = "UPDATE partes SET Cantidad = ? WHERE NumPart = ?";
        Codigo_U = txt_Id.getText().toUpperCase().trim();
        String cant2 = txt_cant.getText().toUpperCase().trim();
        int numEntero = Integer.parseInt(cant2);
        int numEntero2 = Integer.parseInt(cant);
        int tot=numEntero2-numEntero;
        String enteroString = Integer.toString(tot);
        Nombre_U =enteroString;
                
