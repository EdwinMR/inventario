    public void complet()
    {
        String var;
        TextAutoCompleter comple=new TextAutoCompleter(txt_Id);
        try {
            st = cn.createStatement();
            rs = st.executeQuery("SELECT Proveedor FROM partes");
            while (rs.next()) {
                var=rs.getString("Proveedor");
                comple.addItem(var);
            }
        } catch (Exception e) {
            System.out.println("Error En Obtener: " + e.getMessage());
        }
    }
    
    
     public void Mostra_Datos(String n) {
        try {
            DefaultTableModel modelo = (DefaultTableModel) tabla1.getModel();
            modelo.setRowCount(0);
            st = cn.createStatement();
            String x="'";
            rs = st.executeQuery("SELECT * FROM partes where Proveedor="+x+n+x);
            while(rs.next()){
                Vector v = new Vector();
                v.add(rs.getInt(1));
                v.add(rs.getString(2));
                v.add(rs.getString(3));
                v.add(rs.getInt(4));
                v.add(rs.getString(5));
                v.add(rs.getString(6));
                v.add(rs.getString(7));
                v.add(rs.getString(8));
                modelo.addRow(v);
                tabla1.setModel(modelo);
            }
        } catch (Exception e) {
            System.out.println("Error En : " + e.getMessage());
        }
    }
